import Foundation
import UIKit

extension UIColor{
    class func maincolor() -> UIColor{
        return UIColor(red: 0.11, green: 0.13, blue: 0.21, alpha: 1.00)
        
    }
    class func pinkColor() -> UIColor{
        return UIColor(red: 0.91, green: 0.24, blue: 0.40, alpha: 1.00)
        
    }
    class func childColor() -> UIColor{
        return UIColor(red: 0.20, green: 0.20, blue: 0.27, alpha: 1.00)
    }
    
}
